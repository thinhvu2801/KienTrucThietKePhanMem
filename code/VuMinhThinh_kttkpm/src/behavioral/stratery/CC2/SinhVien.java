package behavioral.stratery.CC2;

public class SinhVien {
    private String ten;
    private float diem;

    public SinhVien(String ten, float diem) {
        this.ten = ten;
        this.diem = diem;
    }

    public String getTen() {
        return ten;
    }

    public float getDiem() {
        return diem;
    }
}
